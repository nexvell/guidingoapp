  // Question retrieval helpers
  static Map<String, dynamic>? getQuestionById(int id) {
    try {
      return getAllQuestions().firstWhere((q) => q['id'] == id);
    } catch (e) {
      print('⚠️ Question ID $id not found in question bank');
      return null;
    }
  }

  static List<Map<String, dynamic>> getQuestionsByIds(List<int> ids) {
    final questions = <Map<String, dynamic>>[];
    for (final id in ids) {
      final question = getQuestionById(id);
      if (question != null) {
        questions.add(question);
      }
    }
    return questions;
  }

  // Random selection for quiz sessions
  static List<Map<String, dynamic>> getRandomQuestions(int count) {
    final allQuestions = List<Map<String, dynamic>>.from(getAllQuestions());
    allQuestions.shuffle();
    return allQuestions.take(count).toList();
  }
